def find_recalc():
    cands = []
    if os.environ.get("JSE_XLSX_RECALC"):
        cands.append(os.environ["JSE_XLSX_RECALC"])
    cands += glob.glob("/sessions/*/mnt/.claude/skills/xlsx/scripts/recalc.py")
    cands += glob.glob(os.path.expanduser("~") + "/**/skills/xlsx/scripts/recalc.py", recursive=True)
    for c in cands:
        if os.path.exists(c):
            return c
    return None


def recalc(path):
    script = find_recalc()
    if not script:
        print("WARN: recalc.py not found; skipping recalc. Run tools/verify_deliverable.py separately.", file=sys.stderr)
        return None
    out = subprocess.run([sys.executable, script, path, "60"], capture_output=True, text=True)
    try:
        res = json.loads(out.stdout)
    except json.JSONDecodeError:
        print("WARN: recalc output unparseable:", out.stdout[-300:], file=sys.stderr)
        return None
    return res


