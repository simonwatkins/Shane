def build_scorecard(wb, spec):
    ws = wb.create_sheet("Resilience Scorecard")
    ws.sheet_view.showGridLines = False
    rs.xl_title_block(ws, spec["title"], spec["subtitle"], span=6)
    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 30
    ws.column_dimensions["C"].width = 9
    ws.column_dimensions["D"].width = 9
    ws.column_dimensions["E"].width = 39
    ws.column_dimensions["F"].width = 39
    a_code, b_code = spec["companies"]
    r = 4
    for j, h in enumerate(["Dimension", a_code, b_code, f"Why {a_code}", f"Why {b_code}"]):
        fill = rs.NAVY
        if h == a_code:
            fill = rs.ACCENT
        elif h == b_code:
            fill = rs.ACCENT2
        rs.xl_cell(ws.cell(r, 2 + j, h), bold=True, size=10, color=rs.WHITE, fill=fill, align="center", wrap=True, border=True)
    ws.row_dimensions[r].height = 20
    first = r + 1
    r += 1
    for row in spec["rows"]:
        rs.xl_cell(ws.cell(r, 2, row["dimension"]), size=9, bold=True, fill=rs.LTGREY, wrap=True, border=True)
        rs.xl_cell(ws.cell(r, 3, row["a"]), size=11, bold=True, align="center", color=rs.WHITE, fill=rs.score_fill(row["a"]), border=True)
        rs.xl_cell(ws.cell(r, 4, row["b"]), size=11, bold=True, align="center", color=rs.WHITE, fill=rs.score_fill(row["b"]), border=True)
        rs.xl_cell(ws.cell(r, 5, row["why_a"]), size=8, wrap=True, border=True)
        rs.xl_cell(ws.cell(r, 6, row["why_b"]), size=8, wrap=True, border=True)
        ws.row_dimensions[r].height = 30
        r += 1
    last = r - 1
    rs.xl_cell(ws.cell(r, 2, "AVERAGE SCORE"), size=10, bold=True, color=rs.WHITE, fill=rs.NAVY, border=True)
    ws.cell(r, 3).value = f"=AVERAGE(C{first}:C{last})"
