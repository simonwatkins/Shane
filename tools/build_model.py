def main():
    ap = argparse.ArgumentParser(description="Build a JSE scenario model workbook.")
    ap.add_argument("slugs", nargs="+", help="company slug(s), e.g. shoprite spar")
    ap.add_argument("--out", required=True)
    ap.add_argument("--scorecard", help="comparison scorecard JSON (multi-company)")
    ap.add_argument("--title")
    ap.add_argument("--subtitle", default="Investment Committee working model - internal research, for information only")
    ap.add_argument("--no-recalc", action="store_true")
    args = ap.parse_args()

    companies = []
    for slug in args.slugs:
        comp = load_json(os.path.join(ROOT, "assumptions", f"{slug}.json"))
        comp["_cols"] = flatten_columns(comp["scenarios"])
        comp["_anchor_display"] = ANCHOR_DISPLAY[comp["template"]]
        companies.append(comp)
    # all companies must share the same column layout for the shared Assumptions sheet
    base_cols = companies[0]["_cols"]
    for comp in companies[1:]:
        if [c[1:3] for c in comp["_cols"]] != [c[1:3] for c in base_cols]:
            sys.exit("ERROR: companies have mismatched scenario/year layouts; align assumptions files.")
    macro = load_json(os.path.join(ROOT, "macro", "latest.json"))
    letters = col_letters(len(base_cols))

    title = args.title or f"{companies[0]['name']} - Scenario Model"
    wb = Workbook()
    build_cover(wb, companies, macro, title, args.subtitle)
    if companies and len(companies) > 1 and args.scorecard:
        build_scorecard(wb, load_json(args.scorecard if os.path.isabs(args.scorecard) else os.path.join(ROOT, args.scorecard)))
    build_macro(wb, macro)
    for comp in companies:
        build_actuals(wb, comp)
    reg = build_assumptions(wb, companies, macro, letters)
    for comp in companies:
        build_scenario_model(wb, comp, letters, reg)

    out = args.out if os.path.isabs(args.out) else os.path.join(os.getcwd(), args.out)
    wb.save(out)
    print(f"wrote {out}")
    if not args.no_recalc:
        res = recalc(out)
        if res is not None:
            if res.get("total_errors", 0) > 0:
                print("FORMULA ERRORS:", json.dumps(res.get("error_summary", {}), indent=2))
                sys.exit(2)
            print(f"recalc OK: {res.get('total_formulas')} formulas, 0 errors")


if __name__ == "__main__":
    main()
