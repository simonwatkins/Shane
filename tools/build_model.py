def load_json(path):
    with open(path) as f:
        return json.load(f)


def flatten_columns(scenarios):
    """Return ordered list of (label, key, year, scen_idx, year_idx) data columns."""
    cols = []
    for si, sc in enumerate(scenarios):
        for yi, year in enumerate(sc["years"].keys()):
            short = sc["key"]
            cols.append((f"{short} {year}", sc["key"], year, si, yi))
    return cols


def col_letters(n_data):
    """Anchor in column C (index 3); data columns follow."""
    return [get_column_letter(3 + i) for i in range(n_data + 1)]  # +1 for anchor


def prior_letter(letters, data_idx, scen_idx, year_idx, cols):
    """Excel column letter of the prior period for a given data column."""
    if year_idx == 0:
        return letters[0]  # anchor column C
    # previous year within the same scenario = the data column immediately before
    return letters[data_idx]  # data_idx is 1-based offset (anchor=0), so prev is this-1... handled by caller


# ----------------------------------------------------------------- sheet: Cover
def build_cover(wb, companies, macro, title, subtitle):
    ws = wb.active
    ws.title = "Cover"
    ws.sheet_view.showGridLines = False
    rs.xl_title_block(ws, title, subtitle, span=8)
    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 42
    for c in "CDEFGH":
        ws.column_dimensions[c].width = 15
    r = 4
    blocks = [
        ("HOW TO USE THIS MODEL", "h"),
        ("Change only the BLUE cells on the Assumptions tab. BLACK cells are formulas; GREEN cells link across sheets. Each scenario runs for two forward years against the FY anchor.", "p"),
        ("Generated by tools/build_model.py from assumptions/<slug>.json + macro/latest.json. Do not hand-edit; re-run the generator.", "p"),
        ("", ""),
        ("METHODOLOGY", "h"),
        ("retail-owned: Sale of merchandise = prior x (1 + selling-price inflation + real volume/space). Trading profit = sales x trading margin. Earnings (e) = trading profit - net finance - tax, over a calibrated effective share count so the FY anchor HEPS ties to the reported figure.", "p"),
        ("retail-wholesale: Turnover = prior x (1 + growth). Gross & operating profit = turnover x margin. Continuing HEPS (e) is modelled directly off the actual prints (the reported bridge carries large extraordinary/discontinued items); net debt, leverage and finance costs are modelled explicitly because de-gearing is the equity thesis.", "p"),
        ("", ""),
        ("FY ANCHORS (reported actuals - sacred)", "h"),
    ]
    for text, kind in blocks:
        c = ws.cell(r, 2, text)
        if kind == "h":
            rs.xl_cell(c, bold=True, size=11, color=rs.NAVY)
        elif kind == "p":
            rs.xl_cell(c, size=9, wrap=True)
            ws.row_dimensions[r].height = 34
        ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
        r += 1
    # anchor mini-table
    for comp in companies:
        a = comp["anchor"]
        rs.xl_cell(ws.cell(r, 2, f"{comp['name']} ({comp['jse_code']})  -  {comp['anchor_label']}"),
                   bold=True, size=10, color=rs.WHITE,
                   fill=(rs.ACCENT if comp["template"] == "retail-owned" else rs.ACCENT2))
        ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
        r += 1
        for label, key, fmt in comp["_anchor_display"]:
            rs.xl_cell(ws.cell(r, 2, label), size=9, bold=True, fill=rs.LTGREY, border=True)
            v = a.get(key)
            rs.xl_cell(ws.cell(r, 3, v), size=9, border=True, align="center", fmt=FMT.get(fmt))
            ws.merge_cells(start_row=r, start_column=3, end_row=r, end_column=4)
            rs.xl_cell(ws.cell(r, 5, a.get("source", "")), size=8, italic=True, wrap=True, border=True)
            ws.merge_cells(start_row=r, start_column=5, end_row=r, end_column=8)
            r += 1
        r += 1
    rs.xl_cell(ws.cell(r, 2, "DISCLAIMER"), bold=True, size=11, color=rs.NAVY)
    ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8); r += 1
    rs.xl_cell(ws.cell(r, 2, rs.SACRED_NOTE + " " + rs.DISCLAIMER), size=8, italic=True, wrap=True)
    ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
    ws.row_dimensions[r].height = 60


# ----------------------------------------------------------------- sheet: Macro Deck
def build_macro(wb, macro):
    ws = wb.create_sheet("Macro Deck")
    ws.sheet_view.showGridLines = False
    rs.xl_title_block(ws, macro["title"] + f"  (as at {macro['as_at']})",
                      macro["sources_note"], span=4)
    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 34
    ws.column_dimensions["C"].width = 32
    ws.column_dimensions["D"].width = 40
    r = 4
    last_section = None
    for row in macro["rows"]:
        if row["section"] != last_section:
            rs.xl_cell(ws.cell(r, 2, row["section"]), bold=True, size=11, color=rs.WHITE, fill=rs.ACCENT)
            ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=4)
            r += 1
            last_section = row["section"]
        rs.xl_cell(ws.cell(r, 2, row["metric"]), size=9, bold=True, fill=rs.LTGREY, border=True)
        rs.xl_cell(ws.cell(r, 3, row["value"]), size=9, border=True)
        rs.xl_cell(ws.cell(r, 4, row["source"]), size=9, italic=True, border=True)
        r += 1
    r += 1
    rs.xl_cell(ws.cell(r, 2, "Why it matters: " + macro["why_it_matters"]),
               size=8, italic=True, wrap=True, fill=rs.YELLOW)
    ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=4)
    ws.row_dimensions[r].height = 70


