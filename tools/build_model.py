def build_assumptions(wb, companies, macro, letters):
    ws = wb.create_sheet("Assumptions")
    ws.sheet_view.showGridLines = False
    rs.xl_title_block(ws, "SCENARIO ASSUMPTIONS",
                      "BLUE = input you can change. Forward figures are analyst estimates (e); macro levels are assumptions, not forecasts.")
    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 44
    for L in letters:
        ws.column_dimensions[L].width = 12
    reg = {}  # (slug, driver_key) -> row ; also ("macro", name) -> row

    def header(r, comp=None):
        labels = ["FY2025A"] + [c[0] for c in companies[0]["_cols"]] if comp is None else None
        # build header from the shared column layout
        rs.xl_cell(ws.cell(r, 2, ""), border=True)
        rs.xl_cell(ws.cell(r, 3, companies[0]["anchor_label"]), bold=True, size=9,
                   color=rs.WHITE, fill=rs.MIDGREY, align="center", border=True)
        for j, (label, key, year, si, yi) in enumerate(companies[0]["_cols"]):
            rs.xl_cell(ws.cell(r, 4 + j, label), bold=True, size=9, color=rs.WHITE,
                       fill=rs.scenario_palette(key), align="center", wrap=True, border=True)
        ws.row_dimensions[r].height = 26

    r = 4
    # macro block
    rs.xl_cell(ws.cell(r, 2, "SHARED MACRO (analyst scenario assumptions - see Macro Deck)"),
               bold=True, size=11, color=rs.WHITE, fill=rs.NAVY)
    ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=2); r += 1
    header(r); r += 1
    for name, spec in macro["scenario_levels"].items():
        fmt = FMT.get(spec.get("fmt", "num"))
        rs.xl_cell(ws.cell(r, 2, name), size=9, bold=True, fill=rs.LTGREY, border=True)
        rs.xl_cell(ws.cell(r, 3, spec["FY2025A"]), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=fmt)
        for j, (label, key, year, si, yi) in enumerate(companies[0]["_cols"]):
            val = spec[key][yi]
            rs.xl_cell(ws.cell(r, 4 + j, val), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=fmt)
        r += 1
    r += 1
    # per-company driver blocks
    for comp in companies:
        fill = rs.ACCENT if comp["template"] == "retail-owned" else rs.ACCENT2
        rs.xl_cell(ws.cell(r, 2, f"{comp['name']} ({comp['jse_code']}) - drivers"),
                   bold=True, size=11, color=rs.WHITE, fill=fill)
        ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=2); r += 1
        header(r); r += 1
        a = comp["anchor"]
        for key, label, fmt in DRIVER_SPECS[comp["template"]]:
            rs.xl_cell(ws.cell(r, 2, label), size=9, bold=True, fill=rs.LTGREY, border=True)
            anchor_field = ANCHOR_KEY.get(key)
            anchor_val = a.get(anchor_field) if anchor_field else None
            rs.xl_cell(ws.cell(r, 3, anchor_val), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=FMT.get(fmt))
            for j, (clabel, ckey, year, si, yi) in enumerate(comp["_cols"]):
                yv = comp["scenarios"][si]["years"][year].get(key)
                rs.xl_cell(ws.cell(r, 4 + j, yv), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=FMT.get(fmt))
            reg[(comp["slug"], key)] = r
            r += 1
        # constants needed by formulas (owned template)
        if comp["template"] == "retail-owned":
            for key, label, fmt, val in [
                ("tax_rate", "Effective tax rate (%)", "pct", a["tax_rate"]),
                ("div_cover", "Dividend cover (x)", "mult", a["div_cover"]),
                ("group_rev_ratio", "Group rev / sale-of-merch ratio (x)", "ratio", a["group_rev_ratio"]),
                ("eff_diluted_shares_m", "Effective diluted shares (m)", "num1", a["eff_diluted_shares_m"]),
            ]:
                rs.xl_cell(ws.cell(r, 2, label), size=9, bold=True, fill=rs.LTGREY, border=True)
                for cc in range(3, 3 + 1 + len(comp["_cols"])):
                    rs.xl_cell(ws.cell(r, cc, val), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=FMT.get(fmt))
                reg[(comp["slug"], key)] = r
                r += 1
        else:
            rs.xl_cell(ws.cell(r, 2, "Shares in issue (m) - calibrated (e)"), size=9, bold=True, fill=rs.LTGREY, border=True)
            for cc in range(3, 3 + 1 + len(comp["_cols"])):
                rs.xl_cell(ws.cell(r, cc, a["shares_m"]), size=9, color=rs.INPUT_BLUE, align="center", border=True, fmt=FMT.get("num"))
            reg[(comp["slug"], "shares_m")] = r
            r += 1
        if comp.get("rationale"):
            rs.xl_cell(ws.cell(r, 2, comp["rationale"]), size=8, italic=True, wrap=True, fill=rs.YELLOW)
            ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=2 + len(comp["_cols"]) + 1)
            ws.row_dimensions[r].height = 74
            r += 1
        r += 1
    return reg


def aref(slug, key, col, reg):
    return f"Assumptions!${col}${reg[(slug, key)]}"


# ----------------------------------------------------------------- sheet: Scenario Model
def build_scenario_model(wb, comp, letters, reg):
    ws = wb.create_sheet(f"{comp['jse_code']} Scenario Model")
    ws.sheet_view.showGridLines = False
    fill = rs.ACCENT if comp["template"] == "retail-owned" else rs.ACCENT2
    rs.xl_title_block(ws, f"{comp['name']} - SCENARIO MODEL",
                      "BLACK = formula; GREEN = link to Assumptions. (e) = analyst estimate. FY anchor reproduces reported actuals.")
    ws.column_dimensions["A"].width = 3
    ws.column_dimensions["B"].width = 40
    for L in letters:
        ws.column_dimensions[L].width = 13
    # header
    r = 4
    rs.xl_cell(ws.cell(r, 2, "R'm unless stated"), bold=True, size=9, color=rs.WHITE, fill=rs.NAVY, border=True)
    rs.xl_cell(ws.cell(r, 3, comp["anchor_label"]), bold=True, size=9, color=rs.WHITE, fill=rs.MIDGREY, align="center", border=True)
    for j, (label, key, year, si, yi) in enumerate(comp["_cols"]):
        rs.xl_cell(ws.cell(r, 4 + j, label), bold=True, size=9, color=rs.WHITE, fill=rs.scenario_palette(key), align="center", wrap=True, border=True)
    ws.row_dimensions[r].height = 26
    r += 1
    slug = comp["slug"]
    cols = comp["_cols"]
    datacols = letters[1:]  # exclude anchor col

    def prior_col_for(j):
        si, yi = cols[j][3], cols[j][4]
        if yi == 0:
            return letters[0]
        # previous year same scenario = previous data column
        return datacols[j - 1]

    def line(label, bold=False):
        rs.xl_cell(ws.cell(r, 2, label), size=9, bold=bold, border=True, fill=(rs.LTGREY if bold else None))

    rows = {}
    if comp["template"] == "retail-owned":
        # Sale of merchandise
        line("Sale of merchandise", True); rows["sale"] = r
        rs.xl_cell(ws.cell(r, 3, comp["anchor"]["sale_of_merchandise_rm"]), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        for j, L in enumerate(datacols):
            pc = prior_col_for(j)
            ws.cell(r, 4 + j).value = f"={pc}{rows['sale']}*(1+{aref(slug,'infl',L,reg)}+{aref(slug,'vol',L,reg)})"
            rs.xl_cell(ws.cell(r, 4 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("Group revenue (e)"); rows["rev"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['sale']}*{aref(slug,'group_rev_ratio',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("Gross profit"); rows["gp"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['sale']}*{aref(slug,'gross_margin',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("  Gross margin (%)")
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['gp']}/{L}{rows['sale']}"
            rs.xl_cell(ws.cell(r, 3 + j), size=8, fmt=rs.FMT_PCT, align="center", italic=True, border=True)
        r += 1
        line("Trading profit", True); rows["tp"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['sale']}*{aref(slug,'trading_margin',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True, bold=True)
        r += 1
        line("  Trading margin (%)")
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['tp']}/{L}{rows['sale']}"
            rs.xl_cell(ws.cell(r, 3 + j), size=8, fmt=rs.FMT_PCT, align="center", italic=True, border=True)
        r += 1
        line("less: Net finance costs"); rows["nfc"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"=-{aref(slug,'net_finance_costs_rm',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True, color=rs.LINK_GREEN)
        r += 1
        line("Profit before tax - proxy (e)"); rows["pbt"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['tp']}+{L}{rows['nfc']}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("less: Tax (e)"); rows["tax"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"=-{L}{rows['pbt']}*{aref(slug,'tax_rate',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("Earnings - proxy (e)", True); rows["ear"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['pbt']}+{L}{rows['tax']}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True, bold=True)
        r += 1
        line("Diluted HEPS - proxy (e), cents", True); rows["heps"] = r
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['ear']}/{aref(slug,'eff_diluted_shares_m',L,reg)}*100"
            rs.xl_cell(ws.cell(r, 3 + j), size=10, fmt=rs.FMT_CENT, align="center", border=True, bold=True, fill=rs.LTGREY)
        r += 1
        line("  HEPS growth (%)")
        for j, L in enumerate(letters):
            if j == 0:
                rs.xl_cell(ws.cell(r, 3, "-"), size=8, align="center", italic=True, border=True); continue
            pc = prior_col_for(j - 1)
            ws.cell(r, 3 + j).value = f"={L}{rows['heps']}/{pc}{rows['heps']}-1"
            rs.xl_cell(ws.cell(r, 3 + j), size=8, fmt=rs.FMT_PCT, align="center", italic=True, border=True)
        r += 1
        line("DPS - total (e), cents")
        for j, L in enumerate(letters):
            ws.cell(r, 3 + j).value = f"={L}{rows['heps']}/{aref(slug,'div_cover',L,reg)}"
            rs.xl_cell(ws.cell(r, 3 + j), size=9, fmt=rs.FMT_CENT, align="center", border=True)
        r += 1
    else:  # retail-wholesale
        line("Turnover (continuing)", True); rows["to"] = r
        rs.xl_cell(ws.cell(r, 3, comp["anchor"]["turnover_rm"]), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        for j, L in enumerate(datacols):
            pc = prior_col_for(j)
            ws.cell(r, 4 + j).value = f"={pc}{rows['to']}*(1+{aref(slug,'turnover_growth',L,reg)})"
            rs.xl_cell(ws.cell(r, 4 + j), size=9, fmt=rs.FMT_NUM, align="center", border=True)
        r += 1
        line("  Turnover growth (%)")
        for j, L in enumerate(letters):
            if j == 0:
                rs.xl_cell(ws.cell(r, 3, "-"), size=8, align="center", italic=True, border=True); continue
            pc = prior_col_for(j - 1)
            ws.cell(r, 3 + j).value = f"={L}{rows['to']}/{pc}{rows['to']}-1"
            rs.xl_cell(ws.cell(r, 3 + j), size=8, fmt=rs.FMT_PCT, align="center", italic=True, border=True)
        r += 1
