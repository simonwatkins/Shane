#!/usr/bin/env python3
"""Deterministic scenario-model generator for JSE research.

Builds the styled multi-tab Excel scenario model (Cover, Macro Deck, per-company
Actuals + Scenario Model, shared Assumptions, optional Resilience Scorecard) from
version-controlled inputs:

  assumptions/<slug>.json   per-company anchor (sacred actuals) + scenario drivers
  macro/latest.json         shared, dated macro context + scenario macro levels
  comparisons/<name>.json   (optional) scorecard + verdict for a multi-company run

The financial logic lives in Excel FORMULAS (not hardcoded), so the workbook stays
live: change a blue input on the Assumptions tab and every figure recomputes. This
replaces the ad-hoc one-off build scripts - the structure and house style are now
written and tested once. Mirrors tools/manifest.py's deterministic discipline.

Usage:
  python3 tools/build_model.py shoprite --out model.xlsx
  python3 tools/build_model.py shoprite spar --scorecard comparisons/shoprite-vs-spar.json \\
      --title "Shoprite vs The SPAR Group - Hormuz Scenario Model" --out compare.xlsx

Templates supported: retail-owned (e.g. Shoprite), retail-wholesale (e.g. SPAR).
"""
